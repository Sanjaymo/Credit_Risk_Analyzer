import { Component } from '@angular/core';
import { CommonModule } from '@angular/common';
import { FormsModule } from '@angular/forms';
import { PredictionService, PredictionResult } from '../../services/prediction.service';

@Component({
  selector: 'app-prediction',
  standalone: true,
  imports: [CommonModule, FormsModule],
  templateUrl: './prediction.component.html',
  styleUrl: './prediction.component.css'
})
export class PredictionComponent {
  formData = {
    age: 35,
    monthlyIncome: 5500,
    loanAmount: 15000,
    creditScore: 680,
    employmentYears: 5,
    existingLoans: 1
  };

  result: PredictionResult | null = null;
  loading = false;
  error = '';

  constructor(private predictionService: PredictionService) {}

  onSubmit() {
    this.loading = true;
    this.error = '';
    this.result = null;

    this.predictionService.predict(this.formData).subscribe({
      next: (res) => {
        this.result = res;
        this.loading = false;
      },
      error: (err) => {
        this.error = err.error?.error || 'Prediction failed. Ensure backend and ML services are running.';
        this.loading = false;
      }
    });
  }

  getRiskColor(): string {
    if (!this.result) return '#4361ee';
    if (this.result.riskLevel === 'Low Risk') return '#06d6a0';
    if (this.result.riskLevel === 'Medium Risk') return '#ffd166';
    return '#ef476f';
  }

  getRiskClass(): string {
    if (!this.result) return '';
    if (this.result.riskLevel === 'Low Risk') return 'risk-low';
    if (this.result.riskLevel === 'Medium Risk') return 'risk-medium';
    return 'risk-high';
  }

  getGaugeRotation(): number {
    if (!this.result) return -90;
    return -90 + (this.result.defaultProbability * 180);
  }

  getPercentage(): number {
    if (!this.result) return 0;
    return Math.round(this.result.defaultProbability * 100);
  }

  resetForm() {
    this.result = null;
    this.error = '';
    this.formData = {
      age: 35,
      monthlyIncome: 5500,
      loanAmount: 15000,
      creditScore: 680,
      employmentYears: 5,
      existingLoans: 1
    };
  }

  downloadReport() {
    if (!this.result) return;

    const date = new Date().toLocaleString();
    const report = [
      '═══════════════════════════════════════════════',
      '        SmartLoan Analyzer - Prediction Report',
      '═══════════════════════════════════════════════',
      '',
      `Date: ${date}`,
      '',
      '── Borrower Information ──────────────────────',
      `  Age:               ${this.formData.age} years`,
      `  Monthly Income:    ₹${this.formData.monthlyIncome.toLocaleString('en-IN')}`,
      `  Loan Amount:       ₹${this.formData.loanAmount.toLocaleString('en-IN')}`,
      `  Credit Score:      ${this.formData.creditScore}`,
      `  Employment Years:  ${this.formData.employmentYears}`,
      `  Existing Loans:    ${this.formData.existingLoans}`,
      '',
      '── Risk Assessment ──────────────────────────',
      `  Default Probability: ${(this.result.defaultProbability * 100).toFixed(1)}%`,
      `  Risk Level:          ${this.result.riskLevel}`,
      `  Recommendation:      ${this.result.recommendation}`,
      '',
      '═══════════════════════════════════════════════',
      '  Generated by SmartLoan Analyzer',
      '═══════════════════════════════════════════════',
    ].join('\n');

    const blob = new Blob([report], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `SmartLoan_Report_${new Date().toISOString().slice(0, 10)}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  }
}
